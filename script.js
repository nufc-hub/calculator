function add(array) {
    return array.reduce((total, currentValue) => total + currentValue);
}

function subtract(array) {
    return array.reduce((total, currentValue) => total - currentValue);
}

function multiply (array) {
    return array.reduce((total, currentValue) => total * currentValue);
}

function divide (array) {
    return array.reduce((total, currentValue) => total / currentValue);
}

